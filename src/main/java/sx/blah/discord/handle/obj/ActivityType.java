/**
 * The types a presence can have.
 */
public enum ActivityType {

	/**
	 * The type of playing.
	 * Displays: Playing xxx
	 */
	PLAYING,
	/**
	 * The type of streaming.
	 * Displays: Streaming xxx
	 */
	STREAMING,
	/**
	 * The type of listening.
	 * Displays: Listening to xxx
	 */
	LISTENING,
	/**
	 * The type of watching.
	 * Displays: Watching xxx
	 */
	WATCHING
}
